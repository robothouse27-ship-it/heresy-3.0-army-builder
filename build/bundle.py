#!/usr/bin/env python3
"""Bundle all data/*.json into a single app/data.js so index.html works
offline over file:// (no fetch/CORS issues). Run after editing rules."""
import json, os, glob

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA = os.path.join(ROOT, "data")
OUT  = os.path.join(ROOT, "app", "data.js")
os.makedirs(os.path.dirname(OUT), exist_ok=True)

def load(p):
    return json.load(open(os.path.join(DATA, p), encoding="utf-8"))

units = []
for f in sorted(glob.glob(os.path.join(DATA, "units", "*.json"))):
    if f.endswith("_index.json"):
        continue
    units.append(json.load(open(f, encoding="utf-8")))

bundle = {
    "weapons":      load("weapons.json"),
    "wargearLists": load("wargearLists.json"),
    "slots":        load("slots.json"),
    "detachments":  load("detachments.json"),
    "glossary":     load("glossary.json"),
    "units":        units,
    "meta": {
        "name": "Asuryani — Horus Heresy 3.0 (Playtest)",
        "unitCount": len(units),
        "weaponCount": len(load("weapons.json")),
    },
}

with open(OUT, "w", encoding="utf-8") as fh:
    fh.write("// AUTO-GENERATED by build/bundle.py — do not edit by hand.\n")
    fh.write("window.GAME_DATA = ")
    json.dump(bundle, fh, ensure_ascii=False, separators=(",", ":"))
    fh.write(";\n")

print(f"Wrote {OUT}: {len(units)} units, {len(bundle['weapons'])} weapons")
